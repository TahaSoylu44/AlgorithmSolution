int getMin(int a, int b, int c)
{
    if (a > b)
    {
        if (c > b)
        {
            return b;
        }
        else
        {
            return c;
        }  
    }
    else
    {
        if (a > c)
        {
            return c;
        }
        else
        {
            return a;
        }
    }
}

int squareEdge(vector<vector<int>>& dp, vector<vector<char>>& matrix, int i, int j)
{ 
    if(i == -1) return 0;
    if(j == -1) return 0;

    if (dp[i][j] == -1)
    {
        int result;
        if(matrix[i][j] == '1')
        {
            result = getMin(squareEdge(dp, matrix, i - 1, j), squareEdge(dp, matrix, i, j - 1), squareEdge(dp, matrix, i - 1, j - 1)) + 1;
        }
        else
        {
            result = 0;
        }
        dp[i][j] = result;
        return result;
    }
    else return dp[i][j];
}

int maximalSquare(vector<vector<char>>& matrix)
{
    int ROW = matrix.size();
    int COLUMN = matrix[0].size();

    vector<vector<int>> dp(ROW, vector<int>(COLUMN, -1));

    int maxEdge = 0;
    int currEdge;

    for (int i = 0; i < matrix.size(); i++)
    {
        for (int j = 0; j < matrix[0].size(); j++)
        {
            if (matrix[i][j] == '1')
            {
                currEdge = squareEdge(dp, matrix, i , j);

                if (currEdge > maxEdge)
                {
                    maxEdge = currEdge;
                } 
                if (currEdge == i + 1) break;
            }
        } 
    }
    return maxEdge * maxEdge;
}

